# Desafio 006 - Faça um programa que leia um número e mostre seu dobro, triplo e raiz quadrada.

n = int(input('Digite um número inteiro: '))

print('O dobro do número digitado é: {}'.format(n*2))
print('O triplo do número digitado é: {}'.format(n*3))
print('A raiz quadrada do número digitado é: {}'.format(n**(1/2)))
